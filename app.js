function filter(val){
    if (!val) {
        renderList(ol, getItems(), "li" )
        return false
    }
    t = v => v.toLowerCase().trim()
    const items = getItems().filter(value => t(value).indexOf(t(val)) >= 0)
    renderList(ol, items, "li" )
}
